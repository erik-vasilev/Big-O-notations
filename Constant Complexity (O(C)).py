def constant_algo(items):
    result = items[0] * items[0]
    print()


constant_algo([4, 5, 6, 8])

# In the above script, irrespective of the input size, or the number of items in the input list items,
# the algorithm performs only 2 steps: Finding the square of the first element and printing the result on the screen.
# Hence, the complexity remains constant. -> O(c)
